package JavaSessions_Practice;

public class StringConcatenation {

	public static void main(String[] args) {
		//println keyword is used to print the statement in next or new line
		//print keyword is used to print the statement in the same line
		// always keywords will be starts with same line
		
		int a = 10;
		int b = 20;
		
		String c ="Hello";
		
		double d = 10.22;
		
		char e = '&';
		
		 System.out.println(a+b+c+d+e);
		 
		 System.out.println(c+a+b);
		 
		 System.out.println("sum of a and b is:" + (a+b));
		 
		 System.out.println("sum of a and b is:" + a+b);
		 
		 System.out.print(c);
		 
		 System.out.println(e);
	}

}
